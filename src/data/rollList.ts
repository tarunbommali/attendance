// Helper function to generate attendance data
function generateAttendanceData(subjectCode: string, subjectName: string, instructorName: string, facultyId: number, classCount: number) {
  const startDate = new Date(2024, 0, 15); // Jan 15, 2024
  const attendanceData: {
    id: string;
    date: string;
    subject: string;
    subjectCode: string;
    studentId: string;
    studentName: string;
    status: string;
    instructor: string;
    facultyId: number;
    time: string;
    duration: number;
  }[] = [];
  const students = rollList[0].rollList.slice(0, 10); // Take first 10 students for sample data
  
  for (let i = 0; i < classCount; i++) {
    const classDate = new Date(startDate);
    classDate.setDate(classDate.getDate() + (i * 2)); // Classes every other day
    
    for (const student of students) {
      // Generate random attendance status with higher probability of present
      const randomValue = Math.random();
      let status = "present";
      if (randomValue > 0.85) status = "absent";
      else if (randomValue > 0.75) status = "late";
      
      attendanceData.push({
        id: `${subjectCode}_${student.registrationNumber}_${i}`,
        date: classDate.toISOString().split('T')[0],
        subject: subjectName,
        subjectCode: subjectCode,
        studentId: student.registrationNumber,
        studentName: student.name,
        status: status,
        instructor: instructorName,
        facultyId: facultyId,
        time: "09:00 AM - 10:30 AM",
        duration: 1.5 // hours
      });
    }
  }
  
  return attendanceData;
};